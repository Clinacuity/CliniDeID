    //for each vector, concatenates feature values from a FeatureVector into just 1 String
    //returns list of those strings
    private List<String> makeStringsFromFeatureVectors(List<FeatureVector> correctFeatures) {
        List<String> correctStrings = new ArrayList<>();
        for (FeatureVector featureVector : correctFeatures) {
            String fvString = featureVector.toString().replaceAll("\\s+", " ");
            fvString = fvString.substring(fvString.indexOf("begin"));
            correctStrings.add(fvString);
        }
        return correctStrings;
    }

    @Test
    public void testNer1() {
        //could serialize or copy the resulting strings instead of remaking them
        List<FeatureVector> correctFeatures = makeFvtestNer1();
        List<String> correctStrings = makeStringsFromFeatureVectors(correctFeatures);
        assertTrue(testFeatures("testNer1", correctStrings));
    }

    @Test
    public void testNer2() {
        List<FeatureVector> correctFeatures = makeFvtestNer2();
        List<String> correctStrings = makeStringsFromFeatureVectors(correctFeatures);
        assertTrue(testFeatures("testNer2", correctStrings));
    }

    private boolean testFeatures(String fileName, List<String> correctFeatures) {
        int index = 0;
        boolean fail = false;
        try {
            Serializer.DeserializeJcasFromFile(jCas, TEST_PATH + fileName + ".xmi");
            SimplePipeline.runPipeline(jCas, featureAnnotator);
            for (FeatureVector foundFv : jCas.getAnnotationIndex(FeatureVector.class)) {
                //logger.debug("found: {}-{} correct {}-{}", foundFv.getBegin(), foundFv.getEnd(),correctFeatures.get(index).getBegin(), correctFeatures.get(index).getEnd());
                String found = foundFv.toString();
                found = found.replaceAll("\\s+", " ");
                found = found.substring(found.indexOf("begin"));

                if (!found.equals(correctFeatures.get(index))) {
                    String mytry = correctFeatures.get(index);
                    logger.error("FeatureVector #{} wrong\nCorrect: {}\nFound:   {}", index, correctFeatures.get(index), found);
                    fail = true;
                }
                index++;
            }
            if (fail || index < correctFeatures.size()) {
                logger.error("missing FeatureVectors expected {} but got only {} (fail={})", correctFeatures.size(), index, fail);
                logAnnotations(FeatureVector.class, "FeatureVector");
                return false;
            }
            return true;
        } catch (Exception e) {
            logger.throwing(e);
            return false;
        }
    }
